"""
Integers (int)

Integers are whole numbers: no decimal point.
They can be positive, negative, or zero.
"""

# Positive integers
apples = 5
students = 100
year = 2024

# Negative integers
temperature = -10
debt = -500

# Zero
nothing = 0

print(apples, students, year)
print(temperature, debt)
print(nothing)

# Integer math
print(10 + 3)   # 13
print(10 - 3)   # 7
print(10 * 3)   # 30
print(10 // 3)  # 3 (integer division, no remainder)
print(10 % 3)   # 1 (remainder/modulo)
print(10 ** 3)  # 1000 (10 to the power of 3)

# Large numbers (Python handles them easily!)
big = 1_000_000  # Underscores make big numbers readable
print(big)
