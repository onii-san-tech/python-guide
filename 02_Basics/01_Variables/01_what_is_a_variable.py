"""
What Is a Variable?

A variable is like a box that stores a value.
You give the box a name, and put something inside it.
Later, you can use the name to get the value back.
"""

# Storing a name in a variable
name = "Alice"

# Storing a number in a variable
age = 25

# Using the variables
print(name)
print(age)

# You can change a variable's value
age = 26
print(age)  # Now it's 26
