"""
Naming Variables

Good variable names make code easy to read.
Follow these rules when naming variables.
"""

# Good variable names: clear and descriptive
first_name = "Alice"
last_name = "Smith"
user_age = 30
total_price = 99.99

# Variable names can use letters, numbers, and underscores
player1_score = 100
item_count_2 = 5

# Variable names CANNOT start with a number
# 2fast = "error"  <- this would fail!

# Variable names are case-sensitive
score = 10
Score = 20
SCORE = 30
print(score, Score, SCORE)  # Three different variables!

# Common naming convention: snake_case (words separated by _)
my_favorite_color = "blue"
number_of_students = 25

print(first_name, last_name)
print("Age:", user_age)
