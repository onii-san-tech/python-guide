"""
Defining Functions

A function is a reusable block of code.
Define it once, use it many times.
"""

# Define a function using "def"
def greet():
    print("Hello, World!")

# Call the function
greet()
greet()  # Can call it multiple times!

# Another function
def show_menu():
    print("=== Main Menu ===")
    print("1. Start")
    print("2. Settings")
    print("3. Quit")

show_menu()
