"""
The print() Function

print() is the most basic way to show output.
You will use it constantly while learning Python.
"""

# Printing text (called a "string")
print("Hello!")

# Printing numbers
print(42)
print(3.14)

# Printing multiple things at once
print("My age is", 25)

# Printing nothing creates a blank line
print()

# Printing with a custom separator
print("apple", "banana", "cherry", sep=", ")

# Printing without a newline at the end
print("Same line...", end=" ")
print("still here!")
