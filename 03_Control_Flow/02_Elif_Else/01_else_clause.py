"""
Else Clause

The else block runs when the if condition is False.
It's the "otherwise" part.
"""

age = 15

if age >= 18:
    print("You can vote!")
else:
    print("You're too young to vote.")

# Another example
score = 55

if score >= 60:
    print("You passed!")
else:
    print("You need to study more.")
